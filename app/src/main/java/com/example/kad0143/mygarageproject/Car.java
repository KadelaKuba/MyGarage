package com.example.kad0143.mygarageproject;

public class Car {
    public long id;
    public String brand;
    public String model;
    public String year;
    public String engine;

    public Car(long id, String brand, String model, String year, String engine) {
        this.id = id;
        this.brand = brand;
        this.model = model;
        this.year = year;
        this.engine = engine;
    }
}
